export const shuffleCards = (cards) => {
  return [...cards]
    .map((card) => ({ ...card, flipped: false, matched: false }))
    .sort(() => Math.random() - 0.5);
};

export const getCategories = () => {
  const categories = [...new Set(memoryCards.map((card) => card.category))];
  return categories;
};

export const getCardsByCategory = (category) => {
  const categoryCards = memoryCards.filter(
    (card) => card.category === category
  );

  // Group to ensure we only have complete pairs
  const pairsMap = {};
  categoryCards.forEach((card) => {
    if (!pairsMap[card.back]) {
      pairsMap[card.back] = [];
    }
    pairsMap[card.back].push(card);
  });

  // Only take complete pairs (exactly 2 cards per name)
  const completeCards = [];
  Object.values(pairsMap).forEach((pair) => {
    if (pair.length === 2) {
      completeCards.push(...pair);
    }
  });

  // Shuffle the individual cards
  const shuffledCards = [...completeCards]
    .map((card) => ({ ...card, flipped: false, matched: false }))
    .sort(() => Math.random() - 0.5);

  return shuffledCards;
};

export const getCardsByDifficulty = (difficulty) => {
  let numPairs;
  switch (difficulty) {
    case "easy":
      numPairs = 4;
      break;
    case "medium":
      numPairs = 8;
      break;
    case "hard":
      numPairs = 12;
      break;
    default:
      numPairs = 6;
  }

  // First, group all cards by name to ensure we only get complete pairs
  const allPairsMap = {};
  memoryCards.forEach((card) => {
    if (!allPairsMap[card.back]) {
      allPairsMap[card.back] = [];
    }
    allPairsMap[card.back].push(card);
  });

  // Only take complete pairs (exactly 2 cards per name)
  const completePairs = Object.values(allPairsMap).filter(
    (pair) => pair.length === 2
  );

  // shuffle pairs
  const shuffledPairs = [...completePairs].sort(() => Math.random() - 0.5);

  // take needed number of pairs
  const selectedPairs = shuffledPairs.slice(0, numPairs);

  // flatten cards
  const flatCards = selectedPairs.flat();

  // SHUFFLE individual cards after flattening
  const shuffledCards = [...flatCards]
    .map((card) => ({ ...card, matched: false, flipped: false }))
    .sort(() => Math.random() - 0.5);

  return shuffledCards;
};
